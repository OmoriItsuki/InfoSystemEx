LIST P = PIC18F25K22;
INCLUDE "P18F25K22.INC";

CONFIG FOSC = HSMP;
CONFIG WDTEN = OFF;
CONFIG LVP = OFF;

ORG 0;
BANKSEL TRISB;
CLRF TRISB;
BANKSEL ANSELB;
CLRF ANSELB;
BANKSEL PORTB;
CLRF PORTB;

BANKSEL TRISA;
SETF TRISA;
BANKSEL ANSELA;
CLRF ANSELA;
BANKSEL PORTA;
CLRF PORTA;

LOOP NOP;
BTFSS PORTA,2;
CALL FUNC;
BTFSS PORTA,3;
CALL FUNC2;
GOTO LOOP;

FUNC BTFSS PORTA,3;
CALL SHOW5;
BTFSC PORTA,3;
CALL SHOW2;
RETURN;

FUNC2 BTFSS PORTA,2;
CALL SHOW5;
BTFSC PORTA,2;
CALL SHOW3;
RETURN;

SHOW3 NOP;
CLRF PORTB;
BSF PORTB,0;
BSF PORTB,1;
BSF PORTB,3;
BSF PORTB,2;
BSF PORTB,6;
RETURN


SHOW2 NOP;
CLRF PORTB;
BSF PORTB,0;
BSF PORTB,1;
BSF PORTB,3;
BSF PORTB,4;
BSF PORTB,6;
RETURN

SHOW5 NOP;
CLRF PORTB;
BSF PORTB,0;
BSF PORTB,2;
BSF PORTB,3;
BSF PORTB,5;
BSF PORTB,6;
RETURN


END;







