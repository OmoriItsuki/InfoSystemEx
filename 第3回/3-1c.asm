LIST P = PIC18F25K22;
INCLUDE "P18F25K22.INC";

CONFIG FOSC = HSMP;
CONFIG WDTEN = OFF;
CONFIG LVP = OFF;

ORG 0;
BANKSEL TRISB;
CLRF TRISB;
BANKSEL ANSELB;
CLRF ANSELB;
BANKSEL PORTB;
CLRF PORTB;

MAIN BSF PORTB,0;
CALL FUNC;
BCF PORTB,0;
CALL FUNC;
GOTO MAIN;


FUNC NOP;
CNT EQU H'0';
CNT2 EQU H'1';

MOVLW D'255';
MOVWF CNT
LOOP1 MOVLW D'255';
MOVWF CNT2;
LOOP2 NOP
DECFSZ CNT2,1;
GOTO LOOP2
DECFSZ CNT,1
GOTO LOOP1;
RETURN
END


