LIST P = PIC18F25K22;
INCLUDE "P18F25K22.INC";

CONFIG FOSC = HSMP;
CONFIG WDTEN = OFF;
CONFIG LVP = OFF;

ORG 0;
BANKSEL TRISB;
CLRF TRISB;
BANKSEL ANSELB;
CLRF ANSELB;
BANKSEL PORTB;
CLRF PORTB;

BSF PORTB,0;
BSF PORTB,2;
BSF PORTB,3;
BSF PORTB,4;
BSF PORTB,5;
BSF PORTB,6;

END;

FUNC NOP;
CNT EQU H'0';
CNT2 EQU H'1';

MOVLW D'100';
MOVWF CNT
LOOP1 MOVLW D'100';
MOVWF CNT2;
LOOP2 NOP
DECFSZ CNT2,1;
GOTO LOOP2
DECFSZ CNT,1
GOTO LOOP1;
RETURN
END